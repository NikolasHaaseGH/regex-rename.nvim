local M = {}

local normalMode = require("regex-rename.normalMode")

function dump(o)
   if type(o) == 'table' then
      local s = '{ '
      for k,v in pairs(o) do
         if type(k) ~= 'number' then k = '"'..k..'"' end
         s = s .. '['..k..'] = ' .. dump(v) .. ','
      end
      return s .. '} '
   else
      return tostring(o)
   end
end

function M.rename()
    local result = normalMode.rename()
        --print(dump(result))
end

return M
